template<int T>
void pprint(uint64_t num,
            uint8_t chunk_len = T,
            const std::string& remark = "") {

    if (!remark.empty()) {
        std::cout << remark << ": ";
    }
    std::bitset<T> x(num);
    std::string res = x.to_string();
    for (size_t i = 0; i < T; i++) {
        if (i % chunk_len == 0 && i != 0) {
            std::cout << ' ';
        }
        std::cout << res[i];
    }
    std::cout << '\n';
};


